const splitWords = (words) => {
  let resultArray = [];

  words.forEach((word) => {
    if (word.endsWith(".")) {
      resultArray.push(word.slice(0, -1), ".");
    } else if (word.endsWith(",")) {
      resultArray.push(word.slice(0, -1), ",");
    } else if (word.startsWith("'")) {
      resultArray.push(".", word.slice(1));
    } else {
      var trimmedStr = word.trim();
      resultArray.push(trimmedStr);
    }
  });

  return resultArray;
};

